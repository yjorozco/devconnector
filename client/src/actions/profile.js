import axios from 'axios';
import { setAlert } from './alert';
import {
  GET_PROFILES,
  CLEAR_PROFILE,
  GET_PROFILE,
  PROFILE_ERROR,
  GET_REPOS,
  UPDATE_PROFILE,
} from './type';

export const getCurrentProfile = () => async (dispatch) => {
  try {
    const res = await axios.get('/api/profile/me');
    dispatch({
      type: GET_PROFILE,
      payload: res.data,
    });
  } catch (err) {
    console.log(err);
    dispatch({
      type: PROFILE_ERROR,
      payload: { msg: err.response.statusText, status: err.response.status },
    });
  }
};

